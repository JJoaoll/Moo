-- [@currentFile@]: The file path of the file being parsed. Used to resolve
--                  relative import paths. Should be an absolute or normalized path.
--
-- [@imports@]: List of import paths as strings (e.g., @["std\/list.moo", "utils\/math.moo"]@).
--              These are relative to @currentFile@.
--
-- [@baseProg@]: The program to merge imports into. Usually starts as an empty program
--               or a program with just the definitions from the current file.
--
-- == Return Value
--
-- Returns @IO (Either ImportError Program)@:
--
-- * @Right prog@: All imports processed successfully, merged program returned
-- * @Left err@: An error occurred (file not found, parse error, etc.)
--
-- == Examples
--
-- @
-- -- Process imports for main.moo
-- result <- processImports "src\/main.moo" 
--                          ["utils\/list.moo", "utils\/math.moo"]
--                          emptyProgram
-- case result of
--   Right program -> putStrLn "All imports processed!"
--   Left err      -> print err
-- @
