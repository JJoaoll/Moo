-- == Integration with Import System
--
-- This function is called by 'processImports'' for each imported file:
--
-- @
-- content <- TIO.readFile resolvedPath
-- case parseProgram resolvedPath content of
--   Left err -> pure $ Left $ ParseError resolvedPath (T.pack $ errorBundlePretty err)
--   Right importedProg -> ...
-- @
--
-- The error bundle is converted to 'Text' using 'errorBundlePretty' and
-- wrapped in 'ParseError'.
--
-- == Implementation Notes
--
-- * Delegates to 'PP.program' from "Parser.Program"
-- * Uses 'runParser' from Megaparsec to run the parser
-- * Pure function (no IO)
-- * Type parameters: @T.Text@ (input type), @Void@ (custom error type - none)
--
-- == Why qualified import?
--
-- We import "Parser.Program" qualified as @PP@ to avoid naming conflicts:
--
-- * This module might want its own @program@ function later
-- * Makes it clear we're using the external parser
-- * Prevents accidental shadowing
--
-- == Error Type: Void
--
-- The error type is 'Void' which means:
--
-- * No custom error types are used
-- * Only standard Megaparsec errors can occur
-- * 'Void' is uninhabited (no values of this type exist)
-- * This is the default for simple parsers
--
-- == Performance
--
-- * Parser performance depends on "Parser.Program" implementation
-- * No caching - same file parsed multiple times if imported multiple times
-- * Future optimization: cache parsed programs in a @Map FilePath Program@
--
-- == See Also
--
-- * 'processImports'' - Uses this function to parse imported files
-- * "Parser.Program" - The main program parser being wrapped
-- * 'ParseErrorBundle' - Megaparsec's error type
-- * 'errorBundlePretty' - Converts error bundle to human-readable string
parseProgram :: FilePath -> T.Text -> Either (ParseErrorBundle T.Text Void) Program
parseProgram filepath content = 
  runParser PP.program filepath content

