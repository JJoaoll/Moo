-- | Internal helper function with circular dependency tracking
--
-- = ⚠️  AI-Generated - Not Yet Human-Reviewed
--
-- This is the recursive workhorse of the import system. It maintains a list
-- of visited files to detect circular imports and processes each import in sequence.
--
-- == Base Case
--
-- When the import list is empty (@[]@), returns the accumulated program:
--
-- @
-- processImports' _ [] prog _ = pure $ Right prog
-- @
--
-- == Recursive Case
--
-- When there are imports to process, follows this algorithm:
--
-- 1. __Resolve Path__: Convert relative path to absolute using 'resolveImportPath'
-- 2. __Check Circular__: If @resolvedPath@ is in @visited@, return 'CircularImport'
-- 3. __Check Exists__: If file doesn't exist, return 'FileNotFound'
-- 4. __Read File__: Read file contents using 'TIO.readFile'
-- 5. __Parse File__: Parse using 'parseProgram', return 'ParseError' on failure
-- 6. __Merge Programs__: Merge using 'mergePrograms', return 'DuplicateDefinition' on conflict
-- 7. __Recurse__: Process remaining imports with updated program and visited set
--
-- == Parameters
--
-- [@currentFile@]: The file currently being processed. Used for path resolution.
--
-- [@imports@]: Remaining import paths to process.
--
-- [@prog@]: Accumulated program with all definitions so far.
--
-- [@visited@]: List of file paths already processed. Used for circular import detection.
--              Includes the full path from the original file to the current import chain.
--
-- == Circular Import Detection
--
-- The @visited@ list maintains the entire import chain. When we detect that
-- @resolvedPath `elem` visited@, we return the full chain including the duplicate:
--
-- @
-- CircularImport (visited ++ [resolvedPath])
-- @
--
-- This shows users exactly where the cycle occurs. For example:
--
-- @
-- ["main.moo", "utils.moo", "helpers.moo", "main.moo"]
--                                           ^^^^^^^^ cycle!
-- @
--
-- == Error Propagation
--
-- All errors are propagated immediately using @Either@. The function short-circuits
-- on the first error encountered, which means later imports won't be processed if
-- an earlier one fails.
--
-- == State Threading
--
-- Note how state is threaded through the recursion:
--
-- * @resolvedPath@ becomes the new @currentFile@ (for resolving nested imports)
-- * @rest@ becomes the new import list (one item consumed)
-- * @mergedProg@ becomes the new accumulated program
-- * @resolvedPath:visited@ becomes the new visited set (file added)
--
-- == Implementation Notes
--
-- * Uses 'IO' for file system operations ('doesFileExist', 'TIO.readFile')
-- * Uses 'Either' for error handling (no exceptions thrown)
-- * Processes imports sequentially (not parallel) to maintain deterministic order
-- * Each imported file can itself have imports (recursive)
processImports' :: FilePath
                -> [FilePath]
                -> Program
                -> [FilePath]  -- ^ Already processed files (for cycle detection)
                -> IO (Either ImportError Program)
processImports' _ [] prog _ = pure $ Right prog

processImports' currentFile (importPath:rest) prog visited = do
  -- Step 1: Resolve the import path relative to current file
  let resolvedPath = resolveImportPath currentFile importPath

  -- Step 2: Check for circular imports
  if resolvedPath `elem` visited
    then pure $ Left $ CircularImport (visited ++ [resolvedPath])
    else do
      -- Step 3: Check if file exists
      fileExists <- doesFileExist resolvedPath
      if not fileExists
        then pure $ Left $ FileNotFound resolvedPath
        else do
          -- Step 4: Read and parse the imported file
          content <- TIO.readFile resolvedPath
          case parseProgram resolvedPath content of
            Left err -> pure $ Left $ ParseError resolvedPath (T.pack $ errorBundlePretty err)
            Right importedProg -> do
              -- Step 5: Merge imported program with current program
              case mergePrograms prog importedProg of
                Left conflict -> pure $ Left conflict
                Right mergedProg -> 
                  -- Step 6: Recurse with updated state
                  processImports' resolvedPath rest mergedProg (resolvedPath:visited)

-- | Resolve an import path relative to the current file
--
-- = ⚠️  AI-Generated - Not Yet Human-Reviewed
--
-- Converts a relative import path to an absolute (or normalized) path by
-- resolving it relative to the directory containing the current file.
--
-- == Algorithm
--
-- 1. Extract the directory of @currentFile@ using 'takeDirectory'
-- 2. Combine it with @importPath@ using the path separator '<\/>'
-- 3. Normalize the result using 'normalise' to resolve @..@ and @.@
--
-- == Examples
--
-- === Relative sibling import
--
-- @
-- resolveImportPath "src\/main.moo" "utils.moo"
-- -- Result: "src\/utils.moo"
-- @
--
-- === Subdirectory import
--
-- @
-- resolveImportPath "src\/main.moo" "lib\/list.moo"
-- -- Result: "src\/lib\/list.moo"
-- @
--
-- === Parent directory import
--
-- @
-- resolveImportPath "src\/util\/helper.moo" "..\/core.moo"
-- -- Result: "src\/core.moo"
-- @
--
-- === Multiple parent traversal
--
-- @
-- resolveImportPath "src\/a\/b\/c.moo" "..\/..\/d.moo"
-- -- Result: "src\/a\/d.moo"
-- @
--
-- === Already absolute path (rare)
--
-- @
-- resolveImportPath "src\/main.moo" "\/usr\/lib\/moo\/std.moo"
-- -- Result: "\/usr\/lib\/moo\/std.moo" (normalized)
-- @
--
-- == Path Normalization
--
-- The 'normalise' function from @System.FilePath@:
--
-- * Removes redundant separators: @a\/\/b@ → @a\/b@
-- * Resolves @.@ (current directory): @a\/.\/b@ → @a\/b@
-- * Resolves @..@ (parent directory): @a\/b\/..\/c@ → @a\/c@
-- * Handles platform-specific path conventions
--
-- == Error Handling
--
-- This function is pure and cannot fail. However, the resulting path:
--
-- * May not exist on disk (checked later by 'processImports'')
-- * May escape the project directory if @..@ is used excessively
-- * May refer to system paths if an absolute path is given
--
-- These issues are detected in later stages of import processing.
--
-- == Platform Compatibility
--
-- Uses @System.FilePath@ which handles platform differences:
--
-- * Unix\/Linux: Uses @\/@ as separator
-- * Windows: Uses @\\@ as separator
-- * Works correctly on both platforms
--
-- == Implementation Notes
--
-- * Uses 'let' binding for clarity
-- * Pure function (no IO)
-- * Relies on @System.FilePath@ library functions
-- * Thread-safe and referentially transparent
resolveImportPath :: FilePath -> FilePath -> FilePath
resolveImportPath currentFile importPath = 
  let currentDir = takeDirectory currentFile
  in normalise $ currentDir </> importPath

-- | Merge two programs, checking for definition conflicts
--
-- = ⚠️  AI-Generated - Not Yet Human-Reviewed
--
-- Combines definitions from two programs into one, validating that no names
-- conflict. This is used when merging an imported program with the base program.
--
-- == Conflict Detection Rules
--
-- A conflict occurs when the same name appears in both programs for:
--
-- * __Type definitions__: Two types with the same name
-- * __Data constructors__: Two constructors with the same name (even in different types)
-- * __Global variables__: Two globals with the same name
-- * __Constants__: Two constants with the same name
-- * __Functions__: Two functions with the same name
--
-- If any conflict is detected, returns @Left (DuplicateDefinition ...)@.
--
-- == Merging Strategy
--
-- If no conflicts exist, simply concatenates all definition lists:
--
-- @
-- Program
--   (pGlobals prog1 ++ pGlobals prog2)
--   (pConsts prog1 ++ pConsts prog2)
--   (pFuns prog1 ++ pFuns prog2)
--   (pTypes prog1 ++ pTypes prog2)
-- @
--
-- This means:
--
-- * Order is preserved (prog1 definitions come before prog2)
-- * No deduplication (same definition in both = error)
-- * No shadowing (both definitions would be visible)
--
-- == Examples
--
-- === Successful merge (no conflicts)
--
-- @
-- prog1 = Program
--   { pGlobals = [Global "x" TInt (LInt 5)]
--   , pConsts = []
--   , pFuns = [FunDef "foo" [] TInt [...]]
--   , pTypes = []
--   }
--
-- prog2 = Program
--   { pGlobals = [Global "y" TInt (LInt 10)]
--   , pConsts = []
--   , pFuns = [FunDef "bar" [] TInt [...]]
--   , pTypes = []
--   }
--
-- mergePrograms prog1 prog2
-- -- Result: Right (Program with both x and y, both foo and bar)
-- @
--
-- === Failed merge (function conflict)
--
-- @
-- prog1 = Program { ..., pFuns = [FunDef "add" ...], ... }
-- prog2 = Program { ..., pFuns = [FunDef "add" ...], ... }
--
-- mergePrograms prog1 prog2
-- -- Result: Left (DuplicateDefinition "function 'add'" ...)
-- @
--
-- === Failed merge (constructor conflict)
--
-- @
-- prog1 = Program { ..., pTypes = [TypeDef "T1" [] [ConstrDef "None" []]], ... }
-- prog2 = Program { ..., pTypes = [TypeDef "T2" [] [ConstrDef "None" []]], ... }
--
-- mergePrograms prog1 prog2
-- -- Result: Left (DuplicateDefinition "constructor 'None'" ...)
-- @
--
-- == Limitations
--
-- ⚠️  __File origin tracking not yet implemented__
--
-- The 'DuplicateDefinition' error currently shows @\<unknown file\>@ for both
-- file paths because 'Program' doesn't track where definitions come from.
--
-- To fix this, we would need to either:
--
-- * Extend 'Program' with metadata: @data Program = Program { ..., pSource :: FilePath }@
-- * Pass file paths separately: @mergePrograms :: FilePath -> Program -> FilePath -> Program -> ...@
-- * Use a wrapper type: @data SourcedProgram = SourcedProgram FilePath Program@
--
-- This is marked as a future improvement.
--
-- == Error Messages
--
-- Error messages include the kind of definition and the name:
--
-- * @"type 'MyType'"@
-- * @"constructor 'None'"@
-- * @"global 'myVar'"@
-- * @"const 'PI'"@
-- * @"function 'myFunc'"@
--
-- == Implementation Details
--
-- === Why check constructors separately?
--
-- Constructors from different types must still be unique. In Moo, you can't have:
--
-- @
-- type Option[a] := None | Some(a)
-- type Result[a, b] := None | Error(b)  -- Error! 'None' already used
-- @
--
-- That's why we extract all constructor names across all types and check them.
--
-- === Monadic do notation
--
-- Uses @Either@ monad's @do@ notation for sequencing checks:
--
-- @
-- do
--   checkDuplicates "type" ...
--   checkDuplicates "constructor" ...
--   ...
--   Right $ Program ...
-- @
--
-- If any check returns @Left@, the remaining checks are skipped.
--
-- === Helper function: checkDuplicates
--
-- @
-- checkDuplicates :: String -> [Name] -> [Name] -> Either ImportError ()
-- @
--
-- * Filters @names1@ for items that also appear in @names2@
-- * Returns @Right ()@ if no duplicates found
-- * Returns @Left (DuplicateDefinition ...)@ on first duplicate found
-- * Uses @case@ instead of @if null@ for pattern matching
--
-- == Performance Considerations
--
-- * Uses '(++)'  for list concatenation (O(n) where n is length of first list)
-- * Uses 'elem' for membership check (O(n) where n is length of list)
-- * For large programs with many imports, consider using @Data.Set@ for faster lookups
--
-- Current complexity for each check: O(n × m) where n and m are list sizes.
-- Total complexity: O(total definitions squared) in worst case.
--
-- For small to medium programs (hundreds of definitions), this is acceptable.
--
-- == See Also
--
-- * 'processImports'' - Calls this function to merge imported programs
-- * 'ImportError' - Error types that can be returned
-- * "Grammar.Program" - Definition of 'Program', 'TypeDef', 'FunDef', etc.
mergePrograms :: Program -> Program -> Either ImportError Program
mergePrograms prog1 prog2 = do
  -- Check for duplicate type names
  checkDuplicates "type" (map tName (pTypes prog1)) (map tName (pTypes prog2))
  
  -- Check for duplicate constructor names across all types
  let constrs1 = concatMap (\t -> map cName (tConstrs t)) (pTypes prog1)
  let constrs2 = concatMap (\t -> map cName (tConstrs t)) (pTypes prog2)
  checkDuplicates "constructor" constrs1 constrs2
  
  -- Check for duplicate global names
  checkDuplicates "global" (map gName (pGlobals prog1)) (map gName (pGlobals prog2))
  
  -- Check for duplicate const names
  checkDuplicates "const" (map kName (pConsts prog1)) (map kName (pConsts prog2))
  
  -- Check for duplicate function names
  checkDuplicates "function" (map fName (pFuns prog1)) (map fName (pFuns prog2))
  
  -- If all checks pass, merge the programs
  Right $ Program
    (pGlobals prog1 ++ pGlobals prog2)
    (pConsts prog1 ++ pConsts prog2)
    (pFuns prog1 ++ pFuns prog2)
    (pTypes prog1 ++ pTypes prog2)
  where
    -- | Check for duplicate names between two lists
    --
    -- Takes a kind label (for error messages), two name lists, and returns
    -- either success or a duplicate definition error.
    --
    -- @
    -- checkDuplicates "function" ["foo", "bar"] ["baz", "bar"]
    -- -- Result: Left (DuplicateDefinition "function 'bar'" ...)
    -- @
    checkDuplicates :: String -> [Name] -> [Name] -> Either ImportError ()
    checkDuplicates kind names1 names2 =
      case filter (`elem` names2) names1 of
        [] -> Right ()
        (dup:_) -> Left $ DuplicateDefinition 
          (T.pack kind <> " '" <> dup <> "'")
          "<unknown file>"  -- ⚠️  TODO: Track file origins for better errors
          "<unknown file>"

