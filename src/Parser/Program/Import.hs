= Overview

This module implements a complete import system for the Moo programming language,
allowing programs to reference definitions from other source files. The system
handles file resolution, recursive parsing, conflict detection, and circular
import prevention.

= Import Syntax

The basic syntax for importing a file is:

@
import path/to/file.moo
@

Imports must appear at the beginning of a Moo source file (implementation pending).
Paths can be absolute or relative to the current file.

