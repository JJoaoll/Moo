-- @
-- import path\/to\/file.moo
-- @
--
-- The @import@ keyword must be followed by a file path. The path can contain:
--
-- * Forward slashes (@\/@) for directory separators
-- * Dots (@.@) for file extensions and relative paths (@..@, @.@)
-- * Hyphens (@-@) and underscores (@_@) in file names
