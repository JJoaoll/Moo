-- 1. Resolve the path relative to the current file using 'resolveImportPath'
-- 2. Check if the path is already being processed (circular import check)
-- 3. Verify the file exists using 'doesFileExist'
-- 4. Read and parse the file using 'parseProgram'
-- 5. Recursively process any imports in that file
-- 6. Merge definitions using 'mergePrograms' (with conflict detection)
-- 7. Continue with remaining imports
