-- | Represents errors that can occur during import processing
--
-- = ⚠️  AI-Generated - Not Yet Human-Reviewed
--
-- Each error type captures specific failure modes in the import system.
-- All errors implement 'Eq' and 'Show' for testing and debugging.
--
-- == Error Types
--
-- * 'FileNotFound': Occurs when an import path doesn't resolve to an existing file
-- * 'CircularImport': Occurs when imports form a cycle (A imports B imports A)
-- * 'ParseError': Occurs when an imported file has syntax errors
-- * 'DuplicateDefinition': Occurs when two imports define the same name
--
-- == Examples
--
-- @
-- -- File not found error
-- FileNotFound "utils\/missing.moo"
-- 
-- -- Circular import chain
-- CircularImport ["main.moo", "utils.moo", "helpers.moo", "main.moo"]
-- 
-- -- Parse error in imported file
-- ParseError "lib\/broken.moo" "unexpected token at line 5"
-- 
-- -- Name conflict between imports
-- DuplicateDefinition "function 'add'" "math.moo" "algebra.moo"
-- @
data ImportError
  = FileNotFound FilePath
    -- ^ The imported file doesn't exist at the resolved path.
    -- Contains the full resolved file path that was not found.
    
  | CircularImport [FilePath]
    -- ^ A circular import dependency was detected.
    -- Contains the full chain of imports showing the cycle.
    -- The last element will be the same as an earlier element.
    -- Example: @["a.moo", "b.moo", "c.moo", "a.moo"]@
    
  | ParseError FilePath T.Text
    -- ^ The imported file contains syntax errors.
    -- Contains the file path and the formatted parse error message.
    
  | DuplicateDefinition Name FilePath FilePath
    -- ^ Two imports define the same name, causing a conflict.
    -- Contains: the conflicting name (with kind prefix like "function 'add'"),
    -- and the two file paths where it's defined.
    -- Note: File paths currently show @\<unknown file\>@ - tracking pending.
    
  deriving (Eq, Show)

