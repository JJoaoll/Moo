-- >>> parse importDecl "" "import"
-- Left ... -- Missing path
--
-- >>> parse importDecl "" "import path with spaces.moo"
-- Left ... -- Spaces not allowed in paths
--
-- == Implementation Notes
--
-- * Uses 'keyword' to parse the @import@ keyword
-- * Uses 'lexeme' to consume trailing whitespace
-- * Uses 'takeWhile1P' to parse the path (at least one character required)
-- * Returns the path as a 'FilePath' ('String')
