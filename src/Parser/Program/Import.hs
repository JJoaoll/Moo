-- == Error Handling
--
-- Can return any of the 'ImportError' types:
--
-- * 'FileNotFound' if any import path doesn't exist
-- * 'CircularImport' if imports form a cycle
-- * 'ParseError' if any file has syntax errors
-- * 'DuplicateDefinition' if imports have name conflicts
