= Error Handling

The module defines four types of import errors:

* 'FileNotFound': The imported file doesn't exist on disk
* 'CircularImport': A circular import dependency was detected
* 'ParseError': The imported file contains syntax errors
* 'DuplicateDefinition': Two imports define the same name

All errors include detailed context to help diagnose problems.

= Technical Details

== File Resolution Algorithm

1. Take the directory of the current file being parsed
2. Combine it with the import path using '<\/>'
3. Normalize the result to resolve @..@ and @.@ components

== Import Processing Algorithm

1. Resolve each import path relative to the current file
2. Check if the resolved path is in the visited set (circular check)
3. Verify the file exists on disk
4. Read and parse the file contents
5. Recursively process any imports in the imported file
6. Merge definitions, checking for conflicts
7. Add the file to the visited set

== Conflict Detection

The merger checks for name conflicts in:

* Type definitions ('TypeDef')
* Data constructors ('ConstrDef') 
* Global variables ('GlobalDef')
* Constants ('ConstDef')
* Functions ('FunDef')

If any name appears in both programs, a 'DuplicateDefinition' error is returned.

